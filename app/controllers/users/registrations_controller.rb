  # PUT /resource
  def update
    account_update_params = devise_parameter_sanitizer.sanitize(:account_update)

    # required for settings form to submit when password is left blank
    if account_update_params[:password].blank?
      account_update_params.delete("password")
      account_update_params.delete("password_confirmation")
    end
    @user = User.find(current_user.id)
    if account_update_params[:extra]
      @user.update(extra: @user.extra.merge(account_update_params[:extra]))
      return head :ok
    end
    @update = update_resource(@user, account_update_params)
    if @update
      sign_in @user, bypass: true
    end
    render json: @user.as_json.merge({url: url_for(@user.avatar_path)})
  end

